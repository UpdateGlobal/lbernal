							<h2>Design Canada gana el sitio del mes de abril de 2018</h2>
							<div class="blog-meta">22 de Mayo del 2018 / Luis Bernal</div>
							<p>Locomotive desarrolló el sitio web de Design Canada , en colaboración con Greg Durrell, para apoyar el documental de Design Canada. Una experiencia en línea complementaria a la película que revela de qué se trata el documental; la historia del diseño en Canadá presentada a través de diseñadores destacados y el trabajo icónico animado que hizo sus marcas a lo largo de los años. Permite a Design Canada impulsar contenido exclusivo, promocionar sus proyecciones, vender entradas y tener un destino para todos los medios e iniciativas relacionadas con la prensa.<br>
							La elaboración de este proyecto y su contenido con Greg Durrell en Design Canada fue un tanto desafiante porque su enfoque principal era priorizar la producción real del documental, lo cual tenía sentido para nosotros. El proyecto evolucionó de alguna manera, tuvimos que encontrar soluciones para adaptar nuevos elementos de la historia a lo largo del camino, lo que en realidad fue bastante emocionante para nosotros.<br>
							<b>Arquitectura del servidor</b>
							Al utilizar el almacenamiento en caché estático de la página de Charcoal, un único servidor LAMP modesto fue lo suficientemente abundante como para manejar el intenso tráfico que se viene observando desde el lanzamiento del proyecto. Con la red de entrega de contenido de Cloudflare en la parte superior, la mayoría de los ciclos del servidor se usan al ralentí.<br>
							<b>Tecnologías</b>
							En Locomotive, desarrollamos nuestra propia plantilla de front-end (basada en HTML5, CSS3 y ES6 con sabor a JS). En primer lugar, faculta a nuestros desarrolladores con libertad creativa y, en última instancia, libera el tiempo de desarrollo sin compromisos para probar las últimas tecnologías. Nuestro refinado flujo de trabajo de front-end está acompañado por nuestro marco de backend de probada eficacia y CMS Charcoal . Fácil de usar, tanto para desarrolladores como para clientes, rápido y de código abierto, evoluciona constantemente con cada nuevo proyecto.<br>
							Para Design Canada, Barba.js se utilizó para gestionar las transiciones entre páginas, y realizamos transiciones completas de CSS. WebGL (con three.js ) se aprovechó para crear interacciones lúdicas con la hoja de arce y el logotipo olímpico en la página de inicio, mientras que la animación de entrada y salida de la hoja de arce se gestionó con GSAP (TweenMax). Hemos estado desarrollando nuestro propio administrador de desplazamiento por un tiempo, y potencia nuestra experiencia de desplazamiento de fluidos, poniendo en movimiento, literalmente, una multitud de animaciones de CSS y eventos de JavaScript.<br>
							Nuestra tecnología de back-end (Charcoal, basada en PHP) es muy portátil, por lo que realmente podría haber sido alojada en cualquier lugar, pero decidimos ir con nuestro socio tecnológico de confianza Ecritel Canada.<br><br>

