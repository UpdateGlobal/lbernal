							<h2> Construir el concepto de eCommerce Nubikk</h2>
							<div class="blog-meta">4 de Junio del 2018 / Luis Bernal</div>
							<p>
							Coqtail fue una vez más desafiado a empujar los límites de la navegación de un sitio web de comercio electrónico. Esta vez, fue Nubikk quien nos pidió que analizáramos todos los datos recopilados de los últimos 3 años de su plataforma de comercio electrónico, y obtuviéramos una solución que aumentaría la conversión incluso más que en los últimos años.<br><br>

							Imagine ser guiado a través de una experiencia completa de compras en línea sin un solo desplazamiento. Para la nueva Tienda web de Nubikk lo hicimos realidad; reinventando completamente los métodos estandarizados para navegar por el comercio electrónico.<br><br>

							Seguimos todos los comportamientos de los visitantes (con Hotjar) y se nos ocurrió la solución de hacer que siguieran nuestro ejemplo en lugar de tratar de mantenerlos entretenidos. Al centrarnos únicamente en los zapatos sin un solo desplazamiento, nos aseguramos de no perder visitantes durante su desplazamiento. (Los sitios web tradicionales de comercio electrónico pierden muchos visitantes al sobrecargarlos con demasiada información y profundidad en su oferta de productos).<br><br>
							
							<p>
							Problema 1: la página de detalles del producto.<br><br>

							Así que el concepto fue increíble, la experiencia de desplazamiento cero transcurrió sin problemas a lo largo de nuestra fase de creación de prototipos, pero todo esto fue diseñado sin el contenido real presente. Cuando recibimos el contenido del cliente, nos tomamos un momento para tomar un respiro. No había "bienes inmuebles" para que la página de detalles del producto funcionara tan bien como nos gustaba. Había demasiada copia para mantener la presentación limpia ... volviendo al tablero de dibujo. Después de algunos vodkas nació la idea de crear puntos de conexión en un zapato donde pudiéramos colocar toda la copia. Hicimos un marco de back-end donde el gerente de comercio electrónico es capaz de identificar la ubicación exacta del punto de acceso en el zapato y agregar la información correspondiente.<br><br>
							<img src="../imgpost/nbkk2.jpg"><br><br>

							Problema 2: todo salió bien, ¿qué hay de los dispositivos móviles? <br><br>

							Más del 70% del tráfico proviene de dispositivos móviles. Por lo tanto, era primordial que consideráramos la experiencia móvil y creáramos un flujo intuitivo en tabletas y teléfonos inteligentes. <br><br>

							Incluso hay menos bienes raíces en los teléfonos móviles, por lo que necesitamos una forma de combinar todas las funciones sin complicar demasiado la experiencia del usuario y la interfaz. <br><br>

							Escribimos más de 5,000 líneas de CSS para crear una experiencia fluida para cada dispositivo y tamaño de navegador. Debido a nuestra forma especial de desarrollo al separar el frente y el back-end entre sí, podemos ofrecer soluciones diferentes a medida para diferentes pantallas. <br><br>

							Así que eso es lo que hicimos, volvemos a poner el énfasis en los zapatos, para que un visitante pueda sacar dos cosas: el color y su tamaño. Así que eso es en lo que nos enfocamos para diseñar para dispositivos móviles. El resto de las características las dejamos para que las descubra el curioso comprador. <br><br>
