							<h2> La tecnología se apodera del Mundial Rusia 2018</h2>
							<div class="blog-meta">4 de Junio del 2018 / Luis Bernal</div>

							<p>La tecnología se potencia y asienta con eventos deportivos de gran envergadura, logrando entre otras cosas, que los deportistas alcancen un mejor performance. "La tecnología va a cambiarlo todo, también el deporte. Es momento de que el esfuerzo deportivo se vea acompañado y apoyado por el esfuerzo tecnológico”, mencionó José María Álvarez-Pallete, presidente de Telefónica, en el primer foro global de tecnología y deporte realizado este año en España.<br>
							En esa línea, el mundial es una oportunidad de oro para estrenar nuevos avances. Además, el fútbol es una de las disciplinas que más ha evolucionado gracias a la tecnología: desde balones y camisetas hasta transmisiones y tecnología arbitral. Rusia 2018 no solo será una fiesta futbolera, sino también una vitrina para exponer nuevas herramientas en el deporte rey.</p>

							<h3>Tecnología redonda: Telstar 18</h3>
							<p>
								El elemento principal que da sentido al fútbol no podía quedarse atrás. El balón de fútbol es el que ha sufrido más cambios y mejoras gracias a los constantes avances.<<br>

								Para Rusia se ha creado la denominada Telstar 18, en homenaje al balón usado en México 70 que tenía el mismo nombre que el satélite circular lanzado en 1962. Fue el primero hecho por una marca comercial y desde entonces Adidas ha tenido la misión de elaborar el esférico para todas las citas mundialistas. A diferencia de balones anteriores hechos con 32 paños, la Telstar 18 solo usa 6, estos ya no se cosen sino que se adhieren a altas temperaturas.<br>

								Uno de los puntos más interactivos y novedosos es que este año el balón cuenta con un chip con tecnología de comunicación de campo cercano (NFC). Este permite interactuar con el balón usando el teléfono móvil. Así, se trata de brindar una experiencia completa, interactiva y con contenido adicional para que el hincha pueda vivir esta fiesta mundial sin perderse ningún detalle.<br>
							</p>

							<h3>El arbitraje tecnológico: El VAR y el Big Bang</h3>
							<p>	Uno de los avances más recordados en el fútbol se dio en Brasil 2014 con la tecnología “Goal Control” que permite determinar si el balón cruza totalmente la línea de gol 	gracias a 7 cámaras instaladas en lo alto para cada portería. Esto permite despejar dudas y evitar goles mal cobrados que cambien el rumbo del partido.<br>

								Para Rusia 2018, uno de los avances tecnológicos que más polémica ha generado es el Video Assistant Referee (VAR) o Asistencia Arbitral por Video, en español. Se trata de un conjunto de cámaras que retransmiten una jugada específica y despejan dudas en goles, penales, identificación de jugadores y tarjetas rojas. Funciona con un equipo de 3 árbitros que dispone de imágenes del partido desde distintos ángulos; estos pueden dar advertencias y recibir preguntas del árbitro principal a través de un intercomunicador.</p>



					

