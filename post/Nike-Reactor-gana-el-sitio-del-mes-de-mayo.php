							<p>Sitio del mes May ha sido ganado por DPDK por su experiencia 3D WebGL que nos muestra cómo se siente el nuevo Nike Reactor . Gracias por participar en el proceso de votación y difundir el mensaje, el ganador del Pro Plan del año en el Directorio de awwwards se puede encontrar al final del artículo. Ahora DPDK nos cuenta más sobre la realización de su proyecto ganador de SOTM.<br><br>
							<b>Nike React.</b><br>
							Nike acaba de presentar una revolucionaria innovación en marcha en el mercado, y se llama React. Comenzando como una activación en la tienda y extendiéndose como un primer sitio web móvil, esta campaña temática experimental permite algunas creaciones impresionantes, todo hecho en WebGL . Inspirado por tu experiencia mientras corres en React, te invitamos a visualizar estos sentimientos creando una figura corriendo única. Ofreciendo diferentes categorías de analogías: artículos suaves, ligeros, receptivos y duraderos. Combina los elementos en una figura personalizada que se adapta a tu estilo.<br><br>
							<b>Arquitectura del servidor</b><br>
							Utilizamos Google Compute Engine para configurar un clúster de 4 servidores web que se ejecuta en Ubuntu 16.04 . Delante de estos servidores, ejecutamos un equilibrador de carga de Google HTTPS con comprobaciones de estado para asegurarnos de que todos los servidores responden como deberían. Para que el sitio sea lo más rápido posible en cualquier país, también utilizamos Google Cloud CDN, que se ocupa de los activos estáticos. Junto a estos servidores también ejecutamos un servidor de Base de Datos que almacena los datos del corredor y un servidor de Video Render para los videos compartidos de Nike React. En cuanto al proveedor de hosting, alojamos todos nuestros nuevos proyectos en Google Cloud , por lo que este proyecto no fue tratado de manera diferente.<br>
								<img src="../imgpost/nike4.jpg" style="width: 100%; height: auto;">
							<b>Tecnologías de back-end y front-end</b><br><br>
							<b>Animaciones WebGL</b> <br>
							Todas las animaciones WebGL se hacen usando Three.js . Esta biblioteca 3D se utiliza en la mayoría de nuestros proyectos que contienen animaciones WebGL.<br><br>

							<b>Generación de video</b> <br>
							Para generar los videos de un React Runner, usamos un servidor Socket que se ejecuta en Node . En el front-end capturamos alrededor de 120 fotogramas (la cantidad de fotogramas depende del estilo de ejecución elegido). Estos marcos se envían a Node usando estos sockets web. Finalmente, las imágenes se combinan en un video usando ffmpeg y Node envía un mensaje 'completado' al front-end usando el mismo socket.<br><br>
								<img src="../imgpost/nike2.jpg" style="width: 100%; height: auto;">
							<b>Web stack, frameworks y herramientas.</b><br>
							En DPDK cambiamos a una pila más confiable hace 6 meses. Nos estábamos enfocando más en volver a usar el código que nunca antes, para asegurarnos de cumplir con nuestra fecha límite. Con este proyecto de Nike, también utilizamos esta pila. La pila está impulsada por Javascript en el front-end y, en este caso, un back-end de PHP . El front-end existe de React como nuestra capa de vista y Redux como el almacén de datos en toda nuestra aplicación. Para las animaciones WebGL utilizamos Three.js , que se utiliza en la mayoría de nuestras aplicaciones WebGL. Todas las animaciones de página se realizan mediante animaciones Gsap .<br>

							El back-end se construye usando una configuración personalizada con PhalconPHP, que es un framework PHP. Aquí se configuran y manejan todas las rutas predeterminadas, como los robots y el mapa del sitio. Todas las llamadas API desde la frond-end terminan en nuestros controladores Phalco

